class Furniture{

       static int furnitureId;
	   static String name;
	   static String materialUsed;
	   static String depth;
	   static String height;
	   static String color;
	   
	   static void setFurnitureId(int id){
	      furnitureId = id;
	   }
	   static int getFurnitureId(){
	      return furnitureId;
	   }
	   
	   static void setName(String na){
	      name = na;
	   }
	   static String getName(){
	      return name;
	   }
	   
	   static void setMaterialUsed(String material){
	      materialUsed = material;
	   }
	   static String getMaterialUsed(){
	      return materialUsed;
	   }
	   
	   static void setDepth(String dep){
	      depth = dep;
	   }
	   static String getDepth(){
	      return depth;
	   }
	   
	   static void setHeight(String hgt){
	      height = hgt;
	   }
	   static String getHeight(){
	      return height;
	   }
	   
	   static void setColor(String col){
	      color = col;
	   }
	   static String getColor(){
	      return color;
	   }
	   
	   
	   
	   


}