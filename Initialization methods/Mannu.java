class Mannu{

      static int mannuId;
	  static String name; // heavyclay soil
	  static String density;
	  static String usedFor;

       static void setMannuId(int id){
		   mannuId = id;
	   }
	   static int getMannuId(){
		   return mannuId;
	   }
	   
	   static void setName(String na){
		   name = na;
	   }
	   static String getName(){
		   return name;
	   }
	   
	   static void setDensity(String den){
		  density = den;
	   }
	   static String getDensity(){
		   return density;
	   }
	   
	   static void setUsedFor(String use){
		   usedFor = use;
	   }
	   static String getUsedFor(){
		   return usedFor;
	   }


}