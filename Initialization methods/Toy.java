class Toy{

       static int toyId;
	   static String name;
	   static String color;
	   static String material;
	   static String character;
	   
	   static void setToyId(int id){
		   toyId = id;
	   }
	   static int getToyId(){
		   return toyId;
	   }
	   
	   static void setName(String na){
		   name = na;
	   }
	   static String getName(){
		   return name;
	   }
	   
	   static void setColor(String col){
		  color = col;
	   }
	   static String getColor(){
		   return color;
	   }
	   
	   static void setMaterial(String matr){
		   material = matr;
	   }
	   static String getMaterial(){
		   return material;
	   }
	   
	   static void setCharacter(String cha){
		   character = cha;
	   }
	   static String getCharacter(){
		   return character;
	   }

}